	var n, m, s int
	fmt.Fscan(in, &n, &m, &s)
	s--

	g := make([][]int, n)
	for i := 0; i < m; i++ {
		var u, v int
		fmt.Fscan(in, &u, &v)
		u--
		v--
		g[min(u, v)] = append(g[min(u, v)], max(u, v))
	}

